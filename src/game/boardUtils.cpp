std::vector<std::vector<char>> BoardUtils::stringVectorToCharVector(
    const std::vector<std::string>& stringVector)
{
    std::vector<std::vector<char>> charVector{};
    for (const auto& string : stringVector)
    {
        charVector.emplace_back(string.begin(), string.end());
    }
    return charVector;
}

